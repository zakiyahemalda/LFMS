package model;

public class FileDistribution {
	private String fileDistId;
	private String compId;
	private String fileId;
	
	public String getFileDistId() {
		return fileDistId;
	}
	public void setFileDistId(String fileDistId) {
		this.fileDistId = fileDistId;
	}
	public String getCompId() {
		return compId;
	}
	public void setCompId(String compId) {
		this.compId = compId;
	}
	public String getFileId() {
		return fileId;
	}
	public void setFileId(String fileId) {
		this.fileId = fileId;
	}

}
